                                    <div class="avia_textblock  " style="font-size:18px; " itemprop="text">
                                        <p>The fiduciary duty is the highest standard of care at courts of equity or law. And interestingly, with roots in the 16th century, the word “fiduciary” comes from the Latin fiducia meaning “trust.”</p>
                                        <p>Trust is the foundation. It is essential to our credibility with you and any trier of fact or law.</p>
                                        <p>Through our business litigation practice, national businesses have worked with us directly or through their local attorneys from other states who want to practice before Delaware courts. Moving the admission of out-of-state counsel Pro Hac Vice permitting them to represent clients before Delaware courts, we welcome the opportunity to represent entities from national businesses to family farms in high-stakes litigation. Under Delaware Corporate and LLC law, business stockholders and members have a right to access certain information based on a proper purpose. We can assist in submitting such a demand on management. Should you have a strong case of mismanagement and self-dealing by management, we can assist in bringing an action for breach of fiduciary duty.</p>
                                        <p>Assisting small business owners in disputes with their fellow owners, we have experience in “business divorces.” The solution may be a buy-out, liquidation, compromise or lawsuit to recover damages for mismanagement.</p>
                                        <p>Identifying causes of action, claims, drafting pleadings, fact discovery and motion practice leading up to trials allow us to present your case appropriately to the trier of fact. Raising affirmative defenses, counter-claims and motion practice to defend your case vigorously from the initial response to trial can lead to favorable settlements, where possible and appropriate, and to alternative dispute resolution through mediation or arbitration hearings.</p>
                                        <p>Working for clients in their second or third generation as general counsel, we have helped with a number of routine business matters and annual meetings, including succession planning.</p>
                                        <p>Serving as Delaware counsel in Real Estate transactions with special purpose entity Delaware LLC borrowers, we prepare opinion letters, typically, regarding authority to enter financing transactions, including the drafting of single purpose entity Delaware LLC agreements with a single equity member and another “springing” non-equity member and “independent directors” often required by banks in large Commercial Mortgage Backed Securities in transactions from $5 million to $80 million.</p>
                                        <p>Assisting Delaware business owners with Purchase and Sale Agreements when transitioning into or out of a business, we help negotiate leases, franchise agreements, refinancing, assist with problem customers and difficult vendors as well as annual corporate planning and multigenerational succession planning.</p>

                                        <p>A Beneficiary of a Trust is an individual that receives the benefits of the assets held in Trust. A Trustee is an individual or trust company responsible for managing the assets of the Trust. Typically, the individual who creates the Trust (Settlor) names himself or herself as the Trustee. A Trustee is a fiduciary to the Beneficiaries of the Trust, and therefore, owes fiduciary duties, such as loyalty and care, to the Beneficiaries. After the death of the Settlor of the Trust, the Revocable Trust becomes irrevocable. At that time, the Successor Trustee becomes responsible for annual accounting of a Trust, such as preparing and filing a fiduciary income tax return (IRS Form 1041) if the Trust has taxable income exceeding the minimum threshold (currently, $600).</p>
                                        <p>When we prepare the Revocable Trust, we first name you as both the Trustee and Beneficiary of your own Revocable Trust. Your Trust will benefit you during your lifetime, then after you pass, your Trust will benefit your loved ones – typically, your spouse first, then your children.</p>
                                        <p>You will also name a Successor Trustee for your Trust, usually a spouse, to act as the Trustee in the event you become unable to serve as trustee, for reasons such as incapacity or disability. Your spouse, as Successor Trustee, would then manage the assets for his or her benefit while alive. Then, after the death of the surviving spouse, another Successor Trustee should be named to manage Trust assets for the benefit of minor children, relatives, or friends before the assets are distributed outright to Beneficiaries at specified ages. For example, a typical distribution may distribute one-third of the Trust’s assets when your children reach the age of 25 years old, half of the remaining assets at age 30, then distribute the balance of the remaining assets at age 35.</p>

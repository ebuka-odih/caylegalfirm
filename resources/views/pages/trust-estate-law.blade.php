                                    <div class="avia_textblock  " itemprop="text">
                                        <p>To understand your intentions and explain the process, we schedule a one-hour meeting where we determine the names of your beneficiaries and your fiduciaries. We take great care to listen to our clients to understand your intentions and to provide guidance and suggestions.</p>
                                        <ol>
                                            <li>Your comprehensive estate plan will include the following fundamental documents:
                                                <br>
                                                Revocable Trust Agreement (also known as a Living Trust),
                                            </li>
                                            <li>Last Will and Testament (to fund your Revocable Trust through the Probate process if any of your assets were not funded into your Revocable Trust during your lifetime),</li>
                                            <li>Durable Power of Attorney (for financial decisions while you are alive),</li>
                                            <li>Living Will for Health Care (including power of attorney and directives for healthcare decisions and HIPAA releases), and</li>
                                            <li>Guardianship Designations (for minor children or disabled persons).</li>
                                        </ol>
                                        <p>We then circulate drafts of the estate plan documents for review and comment. We are committed to collaborating with our clients to review and revise the documents until these documents accurately reflect our clients’ wishes.</p>
                                        <p>Next, we schedule a follow-up meeting to execute your documents. We prepare triplicate originals of all of your estate planning documents – one office copy, one for home reference, and a third for your safekeeping. Only the Last Will and Testament is prepared with only one executed original. When you sign your estate plan in our office we provide witnesses and a notary.</p>
                                        <p>Beyond the signing of the estate plan, we also help you implement the plan by advising you how to transfer ownership of assets and change beneficiary designations to fund your new Revocable Trust. If you want us to do this “funding” process for you, our paralegal, Alisa Simpson, has more than 20 years of experience changing ownership of assets and changing beneficiary designations.</p>
                                        <p>We recommend reviewing and updating your estate plan documents every 5 years or after a significant change in circumstances, such as death of a spouse or other beneficiary, divorce, or if your intentions change. Even if your circumstances have not changed, the laws may have changed.  Therefore, a review meeting is important to ensure that your estate plan will work as intended. We can help you prepare amended and restated Revocable Trust agreements in the future to ensure your estate plan is consistent with your goals.</p>
